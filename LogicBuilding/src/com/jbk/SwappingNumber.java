package com.jbk;

public class SwappingNumber {

	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		
		System.out.println("Before Swapping " +a +" "  +b );
		int temp=a;  //temp =10 
		a=b;		//b=10
		b=temp;
		System.out.println("After Swapping " +a + " " +  +b );

		
		

	}

}
