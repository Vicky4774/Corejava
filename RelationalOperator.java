package com.abc;

public class RelationalOperator {

	public static void main(String[] args) 
	{
		int a = 10, b = 13;

		System.out.println(a > b);
		System.out.println(a<b);
		System.out.println(10 < 9);

		System.out.println(9 >= 10); 
		System.out.println(11 <= 10); 
		System.out.println(a == b); 
		System.out.println(10 != 11); 

	}

}
