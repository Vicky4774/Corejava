package com.method;

public class Calculator {

	int num1=40;
	int num2=20;
	
	public int add()
	{
		int sum=num1+num2;
		return sum;
	}
	
	public int sub()
	{
		int sum=num1-num2;
		return sum;
	}
	
	public int mul()
	{
		int sum=num1*num2;
		return sum;
	}
	
	public int div()
	{
		int sum=num1/num2;
		return sum;
	}


}
