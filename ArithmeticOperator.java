package com.abc;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int number_1 = 100;

		int number_2 = 20;

		int add = number_1 + number_2;
		int sub = number_1 - number_2;
		int mul = number_1 * number_2;
		int div = number_1 / number_2;
		int mod = number_1 % number_2;

		System.out.println("The Addition of Two Number Is = " + add);
		System.out.println("The subtraction of Two Number Is = "+ sub);
		System.out.println("The multiplication of Two Number Is = "+mul);
		System.out.println("The division of Two Number Is = " + div);
		System.out.println("The modulo of Two Number Is = " + mod);

	}

}
